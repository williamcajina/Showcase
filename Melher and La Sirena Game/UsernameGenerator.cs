    private static void CheckForDuplicates(string[] array, string arrayName)
    {
        var duplicates = array.GroupBy(x => x)
                              .Where(g => g.Count() > 1)
                              .Select(y => y.Key)
                              .ToList();

        if (duplicates.Count > 0)
        {
            throw new Exception($"Duplicates detected in {arrayName}: {string.Join(", ", duplicates)}");
        }
    }
