    // TODO: We need to calculate the indices to generate the username. We have to alternate between male and female, so the first is ElPrimerAbueloValiente, then we alternate to a female, which would give us LaPrimeraAbuelaValiente.
    public static string GenerateUsernameFromUserCount(int index)
    {
        bool isMale = index % 2 == 0; // Alternate between male and female
        int totalNouns = isMale ? MaleNouns.Length : FemaleNouns.Length;
        int totalAdjectives = isMale ? MaleAdjectives.Length : FemaleAdjectives.Length;
        int totalOrdinals = isMale ? MaleOrdinals.Length : FemaleOrdinals.Length;

        int nounIndex = (index / 2) % totalNouns;
        int adjectiveIndex = ((index / 2) / totalNouns) % totalAdjectives;
        int ordinalIndex = (((index / 2) / totalNouns) / totalAdjectives) % totalOrdinals;

        string username = isMale ? "El" : "La";

        if (isMale)
        {
            username += MaleOrdinals[ordinalIndex] + MaleNouns[nounIndex] + MaleAdjectives[adjectiveIndex];
        }
        else
        {
            username += FemaleOrdinals[ordinalIndex] + FemaleNouns[nounIndex] + FemaleAdjectives[adjectiveIndex];
        }

        return username;
    }



